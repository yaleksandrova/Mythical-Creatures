class Unicorn {
  constructor (name, color){
    this.name = name;
    this.color = color || "white";
  }
  isWhite() {
    return false;
  }
  says(sparklyingStuff) {
    return `**;* ${sparklyingStuff} *;**`;
  }
}

module.exports = Unicorn


























